package session_3_java_operators.challenge;

public class Challenge5 {
    public static void main(String[] args) {
        int x = 30, y = 2;
        x += y;
        System.out.println(x);
        x -= y;
        System.out.println(x);
        x *= y;
        System.out.println(x);
        x /= y;
        System.out.println(x);
        x %= y;
        System.out.println(x);
    }
}
