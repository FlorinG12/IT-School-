package session_3_java_operators.challenge;

public class Challenge4 {
    public static void main(String[] args) {


        int increment = 1;
        int decrement = 10;

        while (increment < 11) {
            System.out.println(increment);
            increment++;
        }
        while (decrement > 0) {
            System.out.println(decrement);
            decrement--;
        }


    }

}
