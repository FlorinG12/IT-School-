package session_3_java_operators.Challenge;

public class Ch5 {
    public static void main(String[] args) {

        int x = 30, y = 2;

        x += y;
        System.out.println(x);
        x -= y;
        System.out.println(x);
        x *= y;
        System.out.println(x);
        x /= y;
        System.out.println(x);
        x %= y;
        System.out.println(x);

    }
}
