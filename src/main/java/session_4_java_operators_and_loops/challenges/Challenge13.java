package session_4_java_operators_and_loops.challenges;

public class Challenge13 {
    public static void main(String[] args) {
        boolean isRaining = true;
        boolean isSunny = false;
        System.out.println(isRaining + " " + isSunny);
        isRaining = !isRaining;
        System.out.println(isRaining + " " + isSunny);
    }
}
